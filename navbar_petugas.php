  <script>
    document.getElementById('logoutMenuLink').addEventListener('click', function(e) {
      e.preventDefault();
  
      fetch('logout.php', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/x-www-form-urlencoded'
          },
          body: ''
        })
        .then(response => {
          if (!response.ok) throw new Error('Network response was not ok');
          return response.json();
        })
        .then(data => {
          const logoutModal = new bootstrap.Modal(document.getElementById('logoutModal'), {
            backdrop: 'static',
            keyboard: false
          });
          const animationContainer = document.getElementById('modal-animation');
          const modalMessage = document.getElementById('modal-message');
          const modalSubmessage = document.getElementById('modal-submessage');
  
          animationContainer.innerHTML = ''; // kosongkan animasi lama
          modalMessage.textContent = '';
          modalSubmessage.textContent = '';
  
          if (data.status === 'success') {
            modalMessage.textContent = data.message || 'Anda berhasil logout';
            modalSubmessage.textContent = 'Terima kasih telah menggunakan aplikasi ini.';
  
            lottie.loadAnimation({
              container: animationContainer,
              renderer: 'svg',
              loop: false,
              autoplay: true,
              path: 'https://assets10.lottiefiles.com/packages/lf20_jbrw3hcz.json' // animasi centang sukses
            });
  
            logoutModal.show();
  
            setTimeout(() => {
              window.location.href = data.redirect || 'login.php';
            }, 1000);
          } else {
            modalMessage.textContent = data.message || 'Logout gagal!';
            modalSubmessage.textContent = '';
  
            lottie.loadAnimation({
              container: animationContainer,
              renderer: 'svg',
              loop: false,
              autoplay: true,
              path: 'https://lottie.host/e04159cf-06e5-4d1a-a514-edf766d5f393/4rvAT0Erjb.json' // animasi silang gagal
            });
  
            logoutModal.show();
  
            setTimeout(() => {
              logoutModal.hide();
            }, 1000);
          }
        })
        .catch(error => {
          console.error('Logout error:', error);
          alert('Terjadi kesalahan saat logout.');
        });
    });
  </script>
